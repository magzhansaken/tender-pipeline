# ─────────────────────────────────────────────────────────────────────────────
# Рендер знаний карточки
# ─────────────────────────────────────────────────────────────────────────────
def _crit(card, levels):
    out = []
    for r in card.get("criticality", []):
        if r.get("level") in levels and r.get("char"):
            val = f" ({r['values']})" if r.get("values") else ""
            out.append(f"{r['char']}{val}")
    return "; ".join(out)


def _subtypes(card):
    subs = []
    for r in card.get("criticality", []):
        if (r.get("level") == "?" and r.get("char") and r.get("values")
                and r["char"] not in ("Подтип", "Характеристика")):
            subs.append(f"«{r['char']}»: {r['values']}")
    return subs


def build_card_system_prompt(card):
    """Системный промпт для товара из топ-1000 (по его карточке). Выход — боевая схема."""
    name = card["name"]
    red = _crit(card, ("🔴",))
    yellow = _crit(card, ("🟡",))
    kz = (card.get("kz") or "").strip()
    subs = _subtypes(card)

    lines = [
        "Ты — система нормализации технических заданий (ТЗ) госзакупок Казахстана.",
        f"Тип товара определён заранее: «{name}»" + (f" (kz: {kz})" if kz else "") + ".",
        "На входе имя лота и сырой текст ТЗ. Извлеки характеристики и собери поисковый запрос.",
        "",
        _RULES,
    ]
    if subs:
        lines.append("⚠️ ЭТО МНОГОЗНАЧНЫЙ ТИП — сначала по ТЗ определи ПОДВИД (часть запроса):")
        for s in subs[:8]:
            lines.append(f"   • {s}")
    if red:
        lines.append(f"🔴 КРИТИЧНЫЕ параметры этого товара — обязательно вынеси в attributes и в "
                     f"search_query, ЕСЛИ есть в ТЗ: {red}")
    if yellow:
        lines.append(f"🟡 Желательные (если указаны): {yellow}")
    if card.get("norm_rules"):
        lines.append("НОРМАЛИЗАЦИЯ: " + card["norm_rules"].replace("\n", " ").strip()[:400])
    if card.get("trash"):
        lines.append("НЕ включай в search_query (мусор/подразумевается маркой): "
                     + card["trash"].replace("\n", " ").strip()[:300])
    # few-shot из примеров карточки
    exs = card.get("examples") or []
    if exs:
        shot = exs[0]
        lines.append(f'Пример запроса: ТЗ «{shot.get("spec","")[:120]}» → search_query: "{shot.get("query","")}"')
    lines.append(f"\nВ поле category ВСЕГДА пиши: «{name}».")
    lines.append("\n" + _SCHEMA)
    return "\n".join(lines)


def build_universal_system_prompt(product_type=None, lot_name=None):
    """Системный промпт для хвоста (нет карточки): категорию модель определяет по шпаргалке."""
    ty = f"Тип (подсказка, может быть неточной): «{product_type}». " if product_type else ""
    return (
        "Ты — система нормализации ТЗ госзакупок Казахстана. Товар РЕДКИЙ, готовой карточки нет.\n"
        f"{ty}Определи товар по ТЗ и имени лота, извлеки характеристики, собери поисковый запрос.\n\n"
        + _RULES +
        "5) ОПИРАЙСЯ НА ТЕКСТ ТЗ, а не на подсказку типа (тип может быть обрезанным).\n"
        "6) Если в ТЗ НЕСКОЛЬКО разных товаров — бери ГЛАВНЫЙ/первый конкретный.\n"
        "7) Определи КАТЕГОРИЮ товара из шпаргалки и запиши её номер+название в поле category; "
        "возьми её 🔴-признаки как обязательные для attributes/search_query.\n\n"
        "ШПАРГАЛКА КАТЕГОРИЙ:\n" + CHEAT_SHEET + "\n\n" + _SCHEMA
    )


# ─────────────────────────────────────────────────────────────────────────────
# Единая точка входа (роутит и отдаёт готовый системный промпт)
# ─────────────────────────────────────────────────────────────────────────────
_ROUTER = None


def _router():
    global _ROUTER
    if _ROUTER is None:
        _ROUTER = CardRouter()
    return _ROUTER


def build_for_tender(lot_name, raw_spec="", use_spec_fallback=False):
    """Роутит тендер и возвращает системный промпт + метаданные."""
    card, ty = _router().route(lot_name or "", raw_spec, use_spec_fallback=use_spec_fallback)
    if card is not None:
        return {"system": build_card_system_prompt(card), "mode": "card",
                "card": card["name"], "type": ty, "category_hint": card["name"]}
    return {"system": build_universal_system_prompt(product_type=ty, lot_name=lot_name),
            "mode": "universal", "card": None, "type": ty, "category_hint": None}



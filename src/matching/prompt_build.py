def build_verify_system_prompt(card=None):
    """Системный промпт верификатора (Вариант Б). Если есть карточка — подставляет
    её 🔴/🟡 признаки как явную строгость для этой категории. Схема ответа НЕ меняется."""
    crit_block = ""
    if card is not None:
        red = _crit(card, ("🔴",))
        yellow = _crit(card, ("🟡",))
        subs = _subtypes(card)
        crit_block = f"\nДЛЯ ТОВАРА «{card['name']}» критичность известна:\n"
        if subs:
            crit_block += "  ⚠️ Многозначный тип — товар должен быть ТОГО ЖЕ подвида, что в ТЗ:\n"
            for s in subs[:6]:
                crit_block += f"     • {s}\n"
        if red:
            crit_block += f"  🔴 КРИТИЧНЫЕ (должны совпасть / не противоречить): {red}\n"
        if yellow:
            crit_block += f"  🟡 ВАЖНЫЕ (если продавец не указал — не повод для NOT_FOUND): {yellow}\n"

    return (
        "Ты — система верификации товаров для госзакупок Казахстана.\n"
        "Тебе дают ТРЕБОВАНИЯ из ТЗ и список КАНДИДАТОВ с маркетплейсов. Определи, какой кандидат подходит.\n"
        f"{crit_block}\n"
        "ПРАВИЛА (важно — не будь излишне строг):\n"
        "1) Бренд/модель бери ТОЛЬКО из данных кандидата (title/snippet). НЕ ПРИДУМЫВАЙ.\n"
        "2) РАЗДЕЛЯЙ критичность:\n"
        "   🔴 КРИТИЧНОЕ — тип товара, типоразмер/размер, марка/модель, код/партномер, главный\n"
        "      рабочий параметр. Если кандидат ПРОТИВОРЕЧИТ по критичному (нужно 205/55 R16, а у него\n"
        "      195/65 R15; нужно «зимняя», а «летняя»; нужно не менее 16, а у него 8) → это conflict.\n"
        "   🟡 ВТОРОСТЕПЕННОЕ — прочие параметры. Если их просто НЕ ВИДНО в коротком описании\n"
        "      кандидата (продавец не расписал), но и ПРОТИВОРЕЧИЯ нет — это НЕ повод для NOT_FOUND.\n"
        "3) РЕШЕНИЕ:\n"
        "   • Кандидат — тот же товар, критичное совпадает, второстепенное тоже подтверждено → FOUND_EXACT.\n"
        "   • Кандидат — тот же товар, критичное совпадает, часть второстепенного продавец НЕ УКАЗАЛ\n"
        "     (без противоречий) → FOUND_PARTIAL (НЕ NOT_FOUND!). Это главный случай — не теряй такие.\n"
        "   • Есть ПРОТИВОРЕЧИЕ по критичному, ИЛИ ни один кандидат не тот товар, ИЛИ все ссылки —\n"
        "     категории/списки → NOT_FOUND.\n"
        "4) matched_specs — что подтверждено; missing_specs — критичное, чего не хватило; "
        "conflicts — реальные противоречия. Не путай «не указано» с «противоречит».\n"
        + _VERIFY_BASE_TAIL
    )


def build_verify_for_tender(lot_name, raw_spec=""):
    """Роутит по имени лота и отдаёт системный промпт верификатора (с карточкой, если есть)."""
    card, ty = _router().route(lot_name or "", raw_spec)
    return {"system": build_verify_system_prompt(card), "mode": "card" if card else "universal",
            "card": card["name"] if card else None, "type": ty}
